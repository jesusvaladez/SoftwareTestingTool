package org.apache.ambari.server;
@java.lang.SuppressWarnings("serial")
public class AmbariException extends java.io.IOException {
    public AmbariException(java.lang.String message) {
        super(message);
    }

    public AmbariException(java.lang.String message, java.lang.Throwable cause) {
        super(message, cause);
    }
}