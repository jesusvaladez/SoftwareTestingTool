package org.apache.ambari.server;
public class AmbariRuntimeException extends java.lang.RuntimeException {
    public AmbariRuntimeException(java.lang.String message, java.lang.Throwable cause) {
        super(message, cause);
    }

    public AmbariRuntimeException(java.lang.String message) {
        super(message);
    }

    public AmbariRuntimeException(java.lang.Throwable cause) {
        super(cause);
    }
}