package org.apache.ambari.server.ldap.service;
public class AmbariLdapException extends java.lang.Exception {
    public AmbariLdapException() {
        super();
    }

    public AmbariLdapException(java.lang.String message) {
        super(message);
    }

    public AmbariLdapException(java.lang.String message, java.lang.Throwable cause) {
        super(message, cause);
    }

    public AmbariLdapException(java.lang.Throwable cause) {
        super(cause);
    }
}